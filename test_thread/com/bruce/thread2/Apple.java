package com.bruce.thread2;

public class Apple {
    private String id;
    private String weigeht;
    
	public Apple(String id, String weigeht) {
		super();
		this.id = id;
		this.weigeht = weigeht;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWeigeht() {
		return weigeht;
	}
	public void setWeigeht(String weigeht) {
		this.weigeht = weigeht;
	}
    
}
